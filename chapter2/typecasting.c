#include <stdio.h>

int main()
{
    int a = 2;
    float b = 56.25;
    a = (int)b; // convert the datatype to int
    printf("%d\n", a);

    // typecasting syntax -->
    // a(variable which has datatype tht has to be converted) = (datatype to be converted) b(vari whose datatype to be converted)
    return 0;
}